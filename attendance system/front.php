Automatic ticket gate with face recognition system in Osaka Metro Morinomiya Station
A facial recognition system is a technology capable of matching a human face from a digital image or a video

frame against a database of faces, typically employed to authenticate users through ID verification services,
 works by pinpointing and measuring facial features from a given image.

Development began on similar systems in the 1960s, beginning as a form of computer application. 
Since their inception, facial recognition systems have seen wider uses in recent times on smartphones 
and in other forms of technology, such as robotics. Because computerized facial recognition involves the measurement of a 
human's physiological 
characteristics, facial recognition systems are categorized as biometrics. Although the accuracy of facial recognition systems as a biometric 
technology is lower than iris recognition and fingerprint recognition, it is widely adopted due to its contactless process.
 Facial recognition
 systems have been deployed in advanced human computer interaction, video surveillance and automatic indexing of images.

Facial recognition systems are employed throughout the world today by governments and private companies.
Their effectiveness varies, and some systems have previously been scrapped because of their ineffectiveness.
 The use of facial recognition systems has also raised controversy, with claims that the systems violate citizens' privacy, commonly
  make incorrect identifications, encourage gender norms and racial profiling, and do not protect important biometric data. These claims
   have led to the ban of facial recognition systems in several cities in the United States. As a result of growing societal concerns, 
   Meta announced that it plans to shut down Facebook facial recognition system, deleting the face scan data of more than one billion users
 This change will represent one of the largest shifts in facial recognition usage in the technology's history.
